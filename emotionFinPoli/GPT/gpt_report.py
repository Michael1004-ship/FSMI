def load_json(source):
    print(f"\nğŸ“‚ {source.upper()} JSON íŒŒì¼ ë¡œë“œ ì¤‘...")
    data = {}
    for fname in required_files:
        path = local_tmp.format(source=source, filename=fname)
        try:
            with open(path, "r", encoding="utf-8") as f:
                key = fname.replace(".json", "")
                data[key] = json.load(f)
                print(f"âœ… {fname} ë¡œë“œ ì™„ë£Œ ({len(str(data[key]))} ë°”ì´íŠ¸)")
        except FileNotFoundError:
            print(f"âŒ íŒŒì¼ì„ ì°¾ì„ ìˆ˜ ì—†ìŒ: {path}")
        except json.JSONDecodeError:
            print(f"âŒ JSON íŒŒì‹± ì˜¤ë¥˜: {path}")
    return data

